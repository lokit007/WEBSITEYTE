package model.bean;

public class ThongKeDanhMuc {
	private int id;
	private String tenDanhMuc;
	private int soDVTT;
	private int soDVC;
	private int soDVTN;
	private int soNCDV;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTenDanhMuc() {
		return tenDanhMuc;
	}
	public void setTenDanhMuc(String tenDanhMuc) {
		this.tenDanhMuc = tenDanhMuc;
	}
	public int getSoDVTT() {
		return soDVTT;
	}
	public void setSoDVTT(int soDVTT) {
		this.soDVTT = soDVTT;
	}
	public int getSoDVC() {
		return soDVC;
	}
	public void setSoDVC(int soDVC) {
		this.soDVC = soDVC;
	}
	public int getSoDVTN() {
		return soDVTN;
	}
	public void setSoDVTN(int soDVTN) {
		this.soDVTN = soDVTN;
	}
	public int getSoNCDV() {
		return soNCDV;
	}
	public void setSoNCDV(int soNCDV) {
		this.soNCDV = soNCDV;
	}
}
