package model.bean;

public class ThongKeTaiKhoan {
	private String taiKhoan;
	private String tenNhaCungCap;
	private int soDichVu;
	private int soNhuCau;
	private int soBaiViet;
	
	public String getTaiKhoan() {
		return taiKhoan;
	}
	public void setTaiKhoan(String taiKhoan) {
		this.taiKhoan = taiKhoan;
	}
	public String getTenNhaCungCap() {
		return tenNhaCungCap;
	}
	public void setTenNhaCungCap(String tenNhaCungCap) {
		this.tenNhaCungCap = tenNhaCungCap;
	}
	public int getSoDichVu() {
		return soDichVu;
	}
	public void setSoDichVu(int soDichVu) {
		this.soDichVu = soDichVu;
	}
	public int getSoNhuCau() {
		return soNhuCau;
	}
	public void setSoNhuCau(int soNhuCau) {
		this.soNhuCau = soNhuCau;
	}
	public int getSoBaiViet() {
		return soBaiViet;
	}
	public void setSoBaiViet(int soBaiViet) {
		this.soBaiViet = soBaiViet;
	}
	
}
